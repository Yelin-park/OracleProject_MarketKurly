-- 주문 성공 시, [장바구니] 삭제 프로시저
CREATE OR REPLACE PROCEDURE mk_p_cart_del
(  
    pc_code customer.c_code%TYPE
)
IS
BEGIN
    DELETE FROM cart
    WHERE c_code = pc_code;
END;
